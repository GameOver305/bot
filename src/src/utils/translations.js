export function t(lang, key, vars = {}) {
  const keys = key.split('.');
  let value = translations[lang];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  if (typeof value !== 'string') return key;
  
  // Replace variables
  for (const [varKey, varValue] of Object.entries(vars)) {
    value = value.replace(`{${varKey}}`, varValue);
  }
  
  return value;
}

